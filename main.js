    for (const [key, value] of Object.entries(data)) {
      if (key.startsWith("settings.")) {
        inlineSettings[key.slice(9)] = value;
      }
    }
    if (Object.keys(inlineSettings).length > 0)
      result.settings = inlineSettings;
    return result;
