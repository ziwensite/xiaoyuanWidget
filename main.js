var StatsCardWidget = class extends BaseWidget {
  getType() {
    return "stats-card";
  }
  async renderContent(container, config) {
    const app = window.app;
    if (!app)
      return;
    const vault = app.vault;
    let files = vault.getMarkdownFiles();
    files = applyFilters(files, config.filters);
    const dimension = config.settings.dimension ?? "total";
    let value;
    let label;
    switch (dimension) {
      case "today": {
        const today = new Date();
        const todayStr = today.toISOString().slice(0, 10);
        value = files.filter((f) => {
          const ctime = new Date(f.stat.ctime);
          return ctime.toISOString().slice(0, 10) === todayStr;
        }).length;
        label = t("stats-today");
        break;
      }
      case "week": {
        const weekAgo = Date.now() - 7 * 24 * 60 * 60 * 1e3;
        value = files.filter((f) => f.stat.ctime > weekAgo).length;
        label = t("stats-week");
        break;
      }
      default: {
        value = files.length;
        label = t("stats-total-notes");
        break;
      }
    }
    container.addClass("xyw-stats-card");
    if (config.title) {
      container.createEl("div", { cls: "xyw-card-title", text: config.title });
    }
    container.createEl("div", { cls: "xyw-card-value", text: String(value) });
    const showLabel = config.settings.showLabel !== "hide";
    if (showLabel) {
      container.createEl("div", { cls: "xyw-card-label", text: label });
    }
  }
};

